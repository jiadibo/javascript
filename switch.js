function testingSwitch(){
    var nilai = 10;

    switch(nilai){
        case 10:
            console.log("Benar Sekali")
            break;
        case 8:
            console.log("Baik")
            break;
        case 6:
            console.log("Cukup baik")
            break;
        default:
            console.log("Buruk")
    }
}